print("ENTER NUM1: ") #number1
num1 = float(input())

print("ENTER OPERATOR: ") #operator
op = input()

print("ENTER NUM2: ") #number2 
num2 = float(input())

if op == "+": #addition
    print(num1 + num2)
elif op == "-": #subtraction
    print(num1 - num2)
elif op == "/": #division
    print(num1 / num2)
elif op == "*": #multiplication
    print(num1 * num2)
    

